// OPERAR CON VARIABLES
// tipos de operadores
// +:suma
// -:resta
// /:division
// *:multiplicacion
// %:resto

// ejemplos
var num1 = 5;
var num2 = 3;

var suma = num1 + num2;
var resta = num1 - num2;
var mult = num1 * num2;
var div = num1 / num2;

//  FUNCIONES DE ENTRADA Y SALIDA EN JS
console.log("La suma es: " + suma);
console.log("La resta es: " + resta);
console.log("La mult es: " + mult);
console.log("La div es: " + div);

// COMPARADORES
// OPERADOR    DESCRIP
// ==          Igual a...
// !=          Diferente a...
// >           Mayor a...
// <           Menor a...
// >=          Mayor o igual a...
// <=          Menor o igual a...

// Cuando comparamos valores o contenidos, el resultado que obtenemos es un valor BOOLEAN (true o false)

Ejemplos:
var n1 = 5;
var n2 = 5;
console.log(n1 == n2);
console.log(n1 != n2);